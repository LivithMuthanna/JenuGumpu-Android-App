    fun get(key: String, lang: String): String {
        val map = if (lang == "kn") kn else en
        return map[key] ?: key
    }

    fun formatNumber(number: String, lang: String): String {
        if (lang != "kn") return number
        val kannadaDigits = charArrayOf('೦', '೧', '೨', '೩', '೪', '೫', '೬', '೭', '೮', '೯')
        return number.map { char ->
            if (char.isDigit()) kannadaDigits[char - '0'] else char
        }.joinToString("")
    }
